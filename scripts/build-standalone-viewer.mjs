// =============================================================================
// scripts/build-standalone-viewer.mjs
//
// Bundles the standalone visualizer (web/src/standalone/viewer.ts) and its
// rendering-engine dependency tree into a single, framework-free
// `visualizer.js`, then copies the accompanying `index.html` and `styles.css`
// into the Python package's web data directory:
//
//     python/src/eigenvue/data/web/{visualizer.js, index.html, styles.css}
//
// These files are the SOURCE OF TRUTH for the viewer shipped by BOTH packages:
// `scripts/bundle-node-data.py` copies them from there into `node/data/web/`.
//
// This is what makes `eigenvue.show()` / `eigenvue.jupyter()` /
// `npx eigenvue show` render the real, animated Canvas visualizations instead
// of a raw JSON dump — the same engine the web app uses.
//
// The output is committed to the repository (like the pre-computed step JSON),
// so packaging never requires a JavaScript build. CI rebuilds it and verifies
// the committed copy is current; esbuild output is deterministic for a pinned
// version, so a stale bundle is caught as a diff.
//
// Usage (from the web/ directory, so esbuild resolves):
//     npm run build:viewer
// or directly:
//     node scripts/build-standalone-viewer.mjs
// =============================================================================

import { createRequire } from "node:module";
import { fileURLToPath } from "node:url";
import path from "node:path";
import fs from "node:fs";

const here = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(here, "..");
const webDir = path.join(repoRoot, "web");
const standaloneDir = path.join(webDir, "src", "standalone");
const outDir = path.join(repoRoot, "python", "src", "eigenvue", "data", "web");

// Resolve esbuild from web/node_modules regardless of the current directory.
const require = createRequire(path.join(webDir, "package.json"));
const esbuild = require("esbuild");

const BANNER =
  "/* Eigenvue standalone visualizer. Generated by " +
  "scripts/build-standalone-viewer.mjs — do not edit by hand. */";

async function main() {
  fs.mkdirSync(outDir, { recursive: true });

  // 1. Bundle the viewer + the entire rendering engine into one IIFE file.
  await esbuild.build({
    entryPoints: [path.join(standaloneDir, "viewer.ts")],
    outfile: path.join(outDir, "visualizer.js"),
    absWorkingDir: webDir,
    bundle: true,
    format: "iife",
    platform: "browser",
    target: ["es2020"],
    minify: true,
    sourcemap: false,
    charset: "utf8",
    legalComments: "none",
    banner: { js: BANNER },
    logLevel: "info",
  });

  // 2. Copy the hand-authored HTML and CSS verbatim.
  for (const asset of ["index.html", "styles.css"]) {
    fs.copyFileSync(path.join(standaloneDir, asset), path.join(outDir, asset));
  }

  const bundle = path.join(outDir, "visualizer.js");
  const sizeKb = (fs.statSync(bundle).size / 1024).toFixed(1);
  console.log(`\nStandalone viewer written to ${path.relative(repoRoot, outDir)}/`);
  console.log(`  visualizer.js  (${sizeKb} KB, minified, includes the rendering engine)`);
  console.log("  index.html");
  console.log("  styles.css");
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
